    private void pintaAcertos(int[] acertos) {
        for (int c = 0; c < jPanelNumeros.getComponentCount(); c++) {
            if (jPanelNumeros.getComponent(c) instanceof JToggleButton) {
                JToggleButton botao = (JToggleButton) (jPanelNumeros.getComponent(c));
                if (botao.isSelected()) {
                    for (int valor : acertos) {
                        if (valor == (Integer.parseInt(botao.getActionCommand()))) {
                            botao.setBackground(Color.DARK_GRAY);
                            botao.setForeground(Color.green);
                        }
                    }
                }
            }
        }
    }

