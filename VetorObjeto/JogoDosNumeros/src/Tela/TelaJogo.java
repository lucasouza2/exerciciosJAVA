    private void seleciona(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleciona
        JToggleButton botao;
        if (evt.getSource() instanceof JToggleButton) {
            botao = (JToggleButton) evt.getSource();
            if (botao.isSelected()) {
                if (totalSelecionado >= 0 && totalSelecionado < 10) {
                    totalSelecionado++;
                } else if (totalSelecionado == 10) {
                    JOptionPane.showMessageDialog(this, "Selecione no maximo 10");
                    botao.setSelected(false);
                }
            } else {
                totalSelecionado--;
            }
            jBVerificarAcertos.setEnabled(totalSelecionado >= 6 && totalSelecionado <= 10);
        }

    }//GEN-LAST:event_seleciona

    private void jBVerificarAcertosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBVerificarAcertosActionPerformed
        aposta = new CartaoAposta(totalSelecionado);
        armazenaAposta();
        sorteio = new CartaoSorteio();
        Arrays.sort(sorteio.getNumeros());
        pintaAcertos(aposta.valoresIguais(sorteio));
        jTResultado.setText("Aposta n° " + aposta.getNumeroAposta() + "\nNúmeros sorteados: " + sorteio.numerosCartao() + "\n"
                + "Valor da Aposta: R$ " + aposta.calculaAporta() + "\nTotal de acertos: " + aposta.totAcertos(sorteio));
        jBVerificarAcertos.setEnabled(false);
        jBJogar.setEnabled(true);
        totalSelecionado = 0;


    }//GEN-LAST:event_jBVerificarAcertosActionPerformed

    private void jBJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBJogarActionPerformed
        jBJogar.setEnabled(false);
        for (int c = 0; c < jPanelNumeros.getComponentCount(); c++) {
            if (jPanelNumeros.getComponent(c) instanceof JToggleButton) {
                JToggleButton botao = (JToggleButton) (jPanelNumeros.getComponent(c));
                if (botao.isSelected()) {
                    botao.setSelected(false);
                    botao.setForeground(Color.black);
                    botao.setBackground(null);
                }
            }
        }
        jTResultado.setText("");
    }//GEN-LAST:event_jBJogarActionPerformed

    private void armazenaAposta() {
        for (int c = 0; c < jPanelNumeros.getComponentCount(); c++) {
            if (jPanelNumeros.getComponent(c) instanceof JToggleButton) {
                JToggleButton botao = (JToggleButton) (jPanelNumeros.getComponent(c));
                if (botao.isSelected()) {
                    int nApostado = Integer.parseInt(botao.getActionCommand());
                    int retApost = aposta.addNumeros(nApostado);
                }
            }
        }
    }

