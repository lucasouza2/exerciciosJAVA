    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed
        if (jTPTipoFigura.getSelectedIndex() == 0) {
            //op√ß√£o ret√¢ngulo
            if (jTFComprimento.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Digite um valor para o campo comprimento", "Aviso", 0);
                jTFComprimento.requestFocusInWindow();
                return;

            }
            if (jTFLargura.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Digite um valor para o campo largura", "Aviso", 0);
                jTFLargura.requestFocusInWindow();
                return;

            }

            float comp = Float.parseFloat(jTFComprimento.getText().trim());
            float larg = Float.parseFloat(jTFLargura.getText().trim());
            figura = new Retangulo(comp, larg); //polimorfismo

        } else if (jTPTipoFigura.getSelectedIndex() == 1) {
            //op√ß√£o circulo
            if (jTFRaio.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Digite um valor para o campo raio", "Aviso", 0);
                jTFRaio.requestFocusInWindow();
                return;
            }

            float raio = Float.parseFloat(jTFRaio.getText());

            figura = new Circulo(raio);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione um tipo de figura", "Aviso", 0);
            return;
        }

        jTAResultado.setText(figura.dados());

    }//GEN-LAST:event_jBCalcularActionPerformed

    private void validaCampo(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validaCampo
        char tecla = evt.getKeyChar();
        if (!Character.isDigit(tecla) && tecla != '.' && tecla != '-') {
            evt.consume();
        }
    }//GEN-LAST:event_validaCampo

