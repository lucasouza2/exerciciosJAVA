    private void limpaCampos() {
        jTFComprimento.setText(null);
        jTFLargura.setText(null);
        jTFRaio.setText(null);
        jTAResultado.setText(null);
        if (jTPTipoFigura.getSelectedIndex() == 0) {
            jTFComprimento.requestFocusInWindow();
        } else if (jTPTipoFigura.getSelectedIndex() == 1) {
            jTFRaio.requestFocusInWindow();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um tipo de Figura", "Aviso", 0);
        }
    }

